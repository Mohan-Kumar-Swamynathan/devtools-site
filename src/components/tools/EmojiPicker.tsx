export default function EmojiPicker() {
  const [search, setSearch] = useState('');
  const [selectedCategory, setSelectedCategory] = useState<string>('Smileys & People');
  const { showToast } = useToast();

  const handleCopy = useCallback((emoji: string) => {
    navigator.clipboard.writeText(emoji).then(() => {
      showToast(`Copied ${emoji} to clipboard!`, 'success');
    });
  }, [showToast]);

  const filteredEmojis = EMOJI_CATEGORIES[selectedCategory as keyof typeof EMOJI_CATEGORIES].filter(emoji =>
    !search || emoji.includes(search)
  );

  return (
    <div className="space-y-6">
      <div className="flex flex-wrap items-center gap-3">
        <div className="flex-1 min-w-[200px]">
          <div className="relative">
            <Search className="absolute left-3 top-1/2 -translate-y-1/2" size={18} style={{ color: 'var(--text-muted)' }} />
            <input
              type="text"
              value={search}
              onChange={(e) => setSearch(e.target.value)}
              className="input w-full pl-10"
              placeholder="Search emojis..."
            />
          </div>
        </div>
      </div>

      <div className="flex flex-wrap gap-2 mb-4">
        {Object.keys(EMOJI_CATEGORIES).map(category => (
          <button
            key={category}
            onClick={() => {
              setSelectedCategory(category);
              setSearch('');
            }}
            className={`px-4 py-2 rounded-lg text-sm font-medium transition-all ${
              selectedCategory === category
                ? 'btn-primary'
                : 'btn-secondary'
            }`}
          >
            {category}
          </button>
        ))}
      </div>

      <div className="p-4 rounded-xl border" style={{
        backgroundColor: 'var(--bg-secondary)',
        borderColor: 'var(--border-primary)'
      }}>
        <div className="grid grid-cols-8 sm:grid-cols-10 md:grid-cols-12 gap-2 max-h-96 overflow-y-auto custom-scrollbar">
          {filteredEmojis.map((emoji, index) => (
            <button
              key={`${emoji}-${index}`}
              onClick={() => handleCopy(emoji)}
              className="text-2xl sm:text-3xl p-2 rounded-lg hover:bg-[var(--bg-tertiary)] transition-all hover:scale-125"
              title={emoji}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}

